# Tuplas #
# Son colecciones de elementos ordenadas que no se pueden modificar #
# El orden de los elemntos de una tupla es igual que el de las listas...
# va de izquierda a derecha de 0 a X #
# *las tuplas van en parentesis* # 
tupla_colores = ('rojo','verde','amarillo')
print(tupla_colores)

# Por cada color en la tupla colores, imprimer color #
for color in tupla_colores:
	print(color)

# Consultar un elemento de la tupla #
print(tupla_colores[2])
# Longitud de una tupla #
print(len(tupla_colores))
# A una tupla no se le pueden hacer ningun tipo de modificaci√≥n #