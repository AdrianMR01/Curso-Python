# Operadores aritmeticos #
num_1 = 10
num_2 = 5
suma = num_1 + num_2 ; print('El resultado de la suma es: ',suma)
resta = num_1 - num_2 ; print('El resultado de la resta es: ',resta)
multiplicacion = num_1 * num_2 ; print('El resultado de la multiplicacion es: ',multiplicacion)
division = num_1 / num_2 ; print('El resultado de la division es: ', division)
residuo = 3 % 8 ; print('El residuo de la operación es: ', residuo)
cociente =  8 // 3 ; print('El cocientes de la operación es: ', cociente)
exponente = num_2 ** num_1 ; print(f'El número {num_2} con exponente {num_1} es: ', exponente)
