# Operadores de asignaci√≥n #
num_1 = 5
num_1 += 4 # Esto quiere decir que a la variable num_1 se le va a sumar un 4...
# ...es como si se tuviera asi num_1 = num_1 + 4 #
print(num_1) 
num_1 -= 2 # Esto quiere decir que a la variable num_1 se le va a restar un 2...
# ...es como si se tuviera asi num_1 = num_1 - 2 #
print(num_1)
num_1 /= 3 # Esto quiere decir que a la variable num_1 se le va a dividir entre 3...
# ...es como si se tuviera asi num_1 = num_1 / 3 #
print(num_1)
num_1 *= 6 # Esto quiere decir que a la variable num_1 se le va a multiplicar por 6...
# ...es como si se tuviera asi num_1 = num_1 * 6 #
print(num_1)
num_1 **= 4 # Esto quiere decir que a la variable num_1 se le va a tiene un exponente 4...
# ...es como si se tuviera asi num_1 = num_1 ** 4 #
print(num_1)
